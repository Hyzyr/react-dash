const DEFAULT_PARAMS = {
  rewriteColumns: {},
};

const useProductsColumns = ({ rewriteColumns } = DEFAULT_PARAMS) => {
  const { t, i18n } = useTranslation("products");
  const columnsData = React.useMemo(() => {
    const tableTitles = t("tableHeaders", { returnObjects: true });

    return columns.map((settings) => {
      let fieldName = settings.field;
      let rewriteCallback = rewriteColumns[fieldName];
      let colData = {
        ...settings,
        headerName:
          tableTitles[settings.headerName].nameShort ?? settings.headerName,
        description:
          tableTitles[settings.headerName].name ?? settings.headerName,
      };

      if (!(rewriteCallback === undefined || !rewriteCallback))
        return rewriteCallback(colData);

      return {
        ...colData,
        cellClassName: ({ row }) =>
          row[fieldName] !== undefined ? "" : "_muted",
      };
    });
  }, [t, i18n]);

  return columnsData;
};
export const cellClassNameGenerator =
  (fieldName) =>
  ({ row }) =>
    row[fieldName] !== undefined ? "" : "_muted";

